package utils

import (
	"encoding/json"
	"fmt"
)

func Ptr[T any](v T) *T {
	return &v
}

func JSONDump(x interface{}) {
	b, err := json.MarshalIndent(x, "", "  ")
	if err != nil {
		fmt.Println("error:", err)
	}
	fmt.Print(string(b))
}

func Contains[T comparable](s []T, e T) bool {
	for _, v := range s {
		if v == e {
			return true
		}
	}
	return false
}
